package t5_for;

public class Test01 {
	public static void main(String[] args) {
		
		// int su; // 선언문
		System.out.println("1안녕");
		System.out.println("2안녕");
		System.out.println("3안녕");
		System.out.println("4안녕");
		System.out.println("5안녕");
		System.out.println("6안녕");
		System.out.println("7안녕");
		System.out.println("8안녕");
		System.out.println("9안녕");
		System.out.println("10안녕");
		System.out.println();
		
		// 반복문
		// for(변수 초기값; 최종값비교; 증감치) { 실행문 }
		
		//for(;;) { 무한 loop
		for(int i=1;i<=10; i+=1) {
			System.out.println(i + "안녕");
			
		}
		
	}
}
